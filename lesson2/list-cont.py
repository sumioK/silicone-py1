# リストの内容を変更する
s = ['a', 'b', 'c', 'd', 'e', 'f', 'g']
print(s[0])

s[0] = 'X'
print(s)

s[2:5] = ['C', 'D', 'E']
print(s)

s[2:5] = []
print(s)

s[:] = []
print(s)

